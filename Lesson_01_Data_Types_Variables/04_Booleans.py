# Booleans are a data type used to represent truth values, either True or False. 
# Booleans are a built-in data type in Python and are represented by the "bool" keyword. 
# Booleans are often used in conditional statements and loops to control the flow of a program. 
# They can also be used to represent the results of comparisons and logical operations. 
# In Python, any non-zero or non-empty value is considered True, while zero or empty values are considered False.

myBoolean = 'True' # this is a string
