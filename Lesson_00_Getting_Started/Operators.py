#----------------------------------------------------------------------------------------------------------------
# Operators
#----------------------------------------------------------------------------------------------------------------
# Operators are special symbols in Python that carry out arithmetic or logical computation.
# The value that the operator operates on is called the operand.
# Ex. 1 + 1 | operand1 operator operand2

# +
# Addition
add = 2 + 2 # = 4

# -
# Subtraction
minus = 5 - 2 # = 3

# * 
# Multiplication
multiply = 3 * 5 # = 15

# //
# Integer (floor) Divsion
# Trunactes decimal portion of result
floor = 22 // 8 # = 2

# /
# True Divsion
# Does not truncate decimal portion of result
division = 22 / 8 # = 2.75

# **
# Exponentiation
exponent = 2 ** 3 # = 8

# %
# Modulus
modulus = 22 % 8 # = 6

# x % 2 will always produce 0 if x is an even number
modulus = 22 % 2 # = 0

# if x % y = 0, then x is a multiple of y
modulus = 25 % 5 # = 0


