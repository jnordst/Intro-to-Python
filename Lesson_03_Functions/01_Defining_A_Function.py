# A function is a block of code that performs a specific task. 
# Functions can be used to organize and modularize code, make code reusable, and make it easier to read and understand.
# A function in Python is defined using the def keyword, followed by the function name, parentheses, and a colon. 
# The code that belongs to the function is indented below the function definition.
# Code inside a function is not automatically run until the function is called.
# To call a function in Python, you simply use the function name, followed by parentheses that contain any necessary arguments. 

# Basic Function Structure
def myFirstFunction():
    pass # "pass" is essentially a "do nothing" statement that tells Python to skip over a block of code without causing any errors.

# Calling the Function
myFirstFunction() # runs any code inside the function
myFirstFunction() # functions can be run and used as many times as you want

# Unlike other programming languages, Python does not require you to specify the data type of the function's arguments or return value.
# In Python, there are no access modifiers like public or private.
# All variables defined inside a function are local to that function and cannot be accessed outside of it.