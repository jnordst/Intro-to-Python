# the len() function is used to get the length of an object. 
# The object can be a string, list, tuple, dictionary, or any other sequence or collection. 
# The len() function returns the number of items in the object.

myList = [5, 13.2, "Hello", True]

len("Hello") # returns 5
len(myList) # returns 4